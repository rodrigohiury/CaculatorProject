public class BracketsPairs {
    
    private int positionOpen = -1;
    private int positionClosed = -1;


    public int getPositionOpen() {
        return positionOpen;
    }
    public void setPositionOpen(int positionOpen) {
        this.positionOpen = positionOpen;
    }
    public int getPositionClosed() {
        return positionClosed;
    }
    public void setPositionClosed(int positionClosed) {
        this.positionClosed = positionClosed;
    }

    
}
